                //document.querySelector("#js-category-content > div.forecast-root > div > div.contentWrap-P1zVT2GT.forecastPage-P1zVT2GT > div.wrap-P1zVT2GT > div.analysisContainer-P1zVT2GT > div.sectionHeader-QrSDBtZ9 > div > div.container-qWcO4bp9.priceLargeContainer-Cimg1AIh.detailsPriceWrap-Cimg1AIh > span.wrap-SNvPvlJ3.changeWrap-qWcO4bp9 > span:nth-child(2)")

